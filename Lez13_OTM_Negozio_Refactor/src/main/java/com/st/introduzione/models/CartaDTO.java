package com.st.introduzione.models;

public class CartaDTO {

	private String cod;
	private String neg;
	private PersonaDTO pro;
	
	public String getCod() {
		return cod;
	}
	public void setCod(String cod) {
		this.cod = cod;
	}
	public String getNeg() {
		return neg;
	}
	public void setNeg(String neg) {
		this.neg = neg;
	}
	public PersonaDTO getPro() {
		return pro;
	}
	public void setPro(PersonaDTO pro) {
		this.pro = pro;
	}
	
	
	
}
